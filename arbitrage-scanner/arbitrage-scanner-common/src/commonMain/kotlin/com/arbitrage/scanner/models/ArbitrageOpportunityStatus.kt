package com.arbitrage.scanner.models

/**
 * Статус арбитражной возможности.
 *
 * Используется для фильтрации арбитражных возможностей по их активности.
 */
enum class ArbitrageOpportunityStatus {
    /**
     * Активная арбитражная возможность (endTimestamp == null).
     * Означает, что возможность все еще доступна.
     */
    ACTIVE,

    /**
     * Неактивная арбитражная возможность (endTimestamp != null).
     * Означает, что возможность была закрыта.
     */
    INACTIVE,

    /**
     * Все арбитражные возможности независимо от статуса.
     * Используется когда не требуется фильтрация по статусу.
     */
    ALL;

    companion object {
        val DEFAULT = ALL
    }
}
